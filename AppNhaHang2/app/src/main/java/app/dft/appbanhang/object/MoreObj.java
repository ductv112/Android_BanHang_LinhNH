public class MoreObj {
    String check, title;

    public MoreObj(String check, String title) {
        this.check = check;
        this.title = title;
    }

    public MoreObj() {
    }

    public String getCheck() {
        return check;
    }

    public void setCheck(String check) {
        this.check = check;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
